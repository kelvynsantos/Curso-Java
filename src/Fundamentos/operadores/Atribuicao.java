package Fundamentos.operadores;

public class Atribuicao {

	public static void main(String[] args) {
		int a = 3; 
		int b = a; 
		 
		b +=a; 
		System.out.println(b); 
		b-=a; 
		System.out.println(b); 
		b *=a; 
		System.out.println(b); 
		b/=a; 
		System.out.println(b); 
		b%=a; 
		System.out.println(b); 
		
	}

}
